package ex_03_Operators;

public class Lab005_Relational_Operator {
    public static void main(String[] args) {
        /*Relational opera :
        * >
        * <
        * equal to
        * ≤
        * ≥
        * non equal to
         */
        int a=10;
        int b=30;
        boolean c=a>b;
        System.out.println(c);

        int age_John=32;
        int age_Monica=20;
        boolean result=age_Monica<age_John;
        System.out.println(result);
    }
}
