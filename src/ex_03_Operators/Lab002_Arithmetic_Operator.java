package ex_03_Operators;

public class Lab002_Arithmetic_Operator {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        float c=3.1f;

        System.out.println(a+b+c);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a-c);
        System.out.println(a*b*c);

    }
}
