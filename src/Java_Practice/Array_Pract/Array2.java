package Java_Practice.Array_Pract;

public class Array2 {
    public static void main(String[] args) {
        int[] marks = new int[4];
        marks[0] = 55;
        marks[1] = 99;
        marks[2] = 77;
        marks[3] = 86;

        System.out.println(marks.length); // 4
        System.out.println(marks[2]); // 77
        System.out.println(marks[3]); // 86

        String[] names = new String[3];
        names[0] = "pramod";
        names[1] = "Ankit";
        names[2] = "Dei";

        System.out.println(names.length); // 3
        System.out.println(names[2]); // dei
    }
}
