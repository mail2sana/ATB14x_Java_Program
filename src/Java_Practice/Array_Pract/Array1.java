package Java_Practice.Array_Pract;

public class Array1 {
    public static void main(String[] args) {
        int[] marks={55,77,99,65,38};

        System.out.println(marks.length); // o'p -> 5
        System.out.println(marks[0]); // 55
        System.out.println(marks[3]); // 65

        String[] names = {"Ankit", "Sana","DK"};
        System.out.println(names.length); // 3
        System.out.println(names[1]); // sana
        System.out.println(names[2]); //DK
    }

}
