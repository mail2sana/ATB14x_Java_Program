package Ex_Java_Basic_If_Else.Java_5Oct_Exercise;

public class Lab03_MaxOf_2numbers {
    public static void main(String[] args) {
        int num1=45;
        int num2=85;
        int max;

        if(num1>num2){
            max=num1;
        }
        else {
            max=num2;
        }
        System.out.println("The maximum of 2 number is " + max);

    }
}
