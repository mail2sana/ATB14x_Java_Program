package ex_08_If_Condition;

public class Lab_If_Elseif_Else {
    public static void main(String[] args) {
        int age=34;
        if(age>34){
            System.out.println("Age is >34");
        }
        else if(age<34){
            System.out.println("Age is <34");
        }
        else{
            System.out.println(age);
        }
    }
}
