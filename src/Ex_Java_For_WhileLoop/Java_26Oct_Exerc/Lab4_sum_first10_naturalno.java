package Ex_Java_For_WhileLoop.Java_26Oct_Exerc;

public class Lab4_sum_first10_naturalno {
    public static void main(String[] args) {
        int n=10;
        int sum=0;

        for (int i = 1; i <=n ; i++) {
            sum +=i;
        }
        System.out.println("The sum of first 10 natural numbers->"+ n + "->  sum of natural ->" + sum);

    }
}
