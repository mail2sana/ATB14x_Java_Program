package ex_04_Operators;

public class Lab0034_Compound_AssignmentOpera {
    public static void main(String[] args) {
        int age=10;
        age+=10; // += -> age=age+10 --> 10+10--> 20
        age-=10;  // -= -> age= age-10 --> 10-10 --> 0
        age/=10; // /=-> age=age/10 -->10/10-->0
        System.out.println(age);

        /*optional to learn digital electronic
        * bitwise - not important for automation
        * ~  - bitwise not
        * >> - right shift
        * >> - left shift
        * ^ - XOR
        * */
    }
}
