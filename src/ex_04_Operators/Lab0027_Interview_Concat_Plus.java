package ex_04_Operators;

public class Lab0027_Interview_Concat_Plus {
    public static void main(String[] args) {
        //+ > arithmetic operator
        int a=10;
        int b=20;
        System.out.println(a+b);
        System.out.println(a-b);
        //+ string if it is a string it will Concatenation (combine the words)
        String S1="Sangita";
        String S2="Nata";
        System.out.println(S1+S2);
        //System.out.println(S1-S2); minus, *,/ not support for string in concatenate
    }
}
