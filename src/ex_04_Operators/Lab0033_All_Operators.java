package ex_04_Operators;

public class Lab0033_All_Operators {
    public static void main(String[] args) {
       // assignment operators
        boolean b1=true;

        int a=10;
        int b=20;

        // Arithmentic
        System.out.println(a+b);
        // relational operator
        System.out.println(a>b);
        //  operator
        System.out.println(a>=b);
        // compare operator
        System.out.println(a==b);
        // reverse
        System.out.println(a!=b);
    }

}
