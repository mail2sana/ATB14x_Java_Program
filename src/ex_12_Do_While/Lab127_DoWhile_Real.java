package ex_12_Do_While;

public class Lab127_DoWhile_Real {
    public static void main(String[] args) {
        //web automation
        int number=0;
        //0-> it can be 0 to 10 - elements or students
        do{
            System.out.println(number);
            // call function(number)
            number++;
        }while (number<0);
    }
}
